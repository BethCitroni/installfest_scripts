##Checkpoints to see if you computer is configured properly.

# When you type `brew doctor` into your Terminal, you get no warnings.  If you get warnings follow the instructions and repeat until it says `Your system is ready to brew.`

# When you type `ruby -v` into your terminal, you get `ruby  2.1.0`

# When you type `pry` into your terminal, it changes the prompt to `pry(main)`
# (Type `exit` to return to return to the bash prompt)
gem list pry -i # returns true
# When you type `subl` into your terminal, it opens up Sublime Text

# You have your `.gitignore_global`, `.gitconfig`, `.bash_profile` set up in your root directory. You should be able to open each one with `subl ~/FILENAME` ie `subl ~/.gitignore_global`

# When you have Sublime open and press `CMD+Shift+P` you'll see a drop down menu. When you type `install` into the menu, you should see the option `Package Control: Install Package`

# Your ssh keys are set up (see [here](https://help.github.com/articles/generating-ssh-keys) for instructions)

# When you type `subl ~/dev/wdi/WDI_NYC_12`, you see our class repository files

# You have `Spectacle` installed on your computer and can use it to manage your windows
